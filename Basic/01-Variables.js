//variables psoibilidad de guardar
/*
var -> lowerCamelCase es la manera original de declarar variables
no es recomendado manejar variables con ellas
let -> es lo mismo que var pero tiene ambito 
const-> son para valores que no se cambiaran pero no es que no sea inmutable sus 
referencia no puede cambiar pero sus datos si
*/
//var
var helloWord=5
console.log(helloWord)
helloWord="hola7"
console.log(helloWord)

//let

let hellWord2="hola de nuevo"
console.log(hellWord2)
hellWord2=5
console.log(hellWord2)

//const

const hellWord3=5
console.log(hellWord3)

